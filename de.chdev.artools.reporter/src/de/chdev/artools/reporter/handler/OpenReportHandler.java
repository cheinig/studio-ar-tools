package de.chdev.artools.reporter.handler;

import org.eclipse.core.commands.AbstractHandler;
import org.eclipse.core.commands.ExecutionEvent;
import org.eclipse.core.commands.ExecutionException;
import org.eclipse.ui.handlers.HandlerUtil;

import de.chdev.artools.reporter.ui.CreateReportDialog;

/**
 * @author Christoph Heinig
 *
 */
public class OpenReportHandler  extends AbstractHandler{

    /* (non-Javadoc)
     * @see org.eclipse.core.commands.IHandler#execute(org.eclipse.core.commands.ExecutionEvent)
     */
    @Override
    public Object execute(ExecutionEvent event) throws ExecutionException {
	
	CreateReportDialog dialog = new CreateReportDialog(HandlerUtil
		.getActiveWorkbenchWindow(event).getShell());
	
	int returnValue = dialog.open();

	return null;
    }

}
